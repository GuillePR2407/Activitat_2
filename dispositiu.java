class Dispositiu {
    private String marca;
    private String model;
    private int preuBase;

    public String getMarca(){return marca;}
    public void setMarca(String newMarca){marca = newMarca;}

    public String getModel(){return model;}
    public void setModel(String newModel){model = newModel;}

    public int getPreuBase(){return preuBase;}
    public void setPreuBase(int newPreuBase){preuBase = newPreuBase;}
}